    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        String nome, area;
        Cursos objCurso = new Cursos();
        nome = txtCurso.getText();
        area = txtArea.getText();
        objCurso.setNome_Curso(nome);
        objCurso.setArea(area);
        if (objCurso.getNome_Curso().trim().equals("") || objCurso.getArea().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {
            CursosDAO objCursosdao = new CursosDAO();
            objCursosdao.cadastrarCurso(objCurso);
            readTbl();
        }
        limparCampos();
    }//GEN-LAST:event_btnCadastroActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String idcurso = txtId.getText();
        if (idcurso.equals("")) {
            JOptionPane.showMessageDialog(null, "Carregue o campo que deseja editar!");
        } else {
            alterarCampo();
            readTbl();
            limparCampos();
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        String idcurso = txtId.getText();
        if (idcurso.equals("")) {
            JOptionPane.showMessageDialog(null, "Carregue o campo que deseja deletar!");
        } else {
            deletarCampo();
            readTbl();
            limparCampos();
        }

    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        limparCampos();
    }//GEN-LAST:event_btnLimparActionPerformed
    public void readTbl() {
        tableModel = (DefaultTableModel) tblCursos.getModel();
        tableModel.setNumRows(0);
        CursosDAO cursosDAO = new CursosDAO();
        List<Cursos> cursos = cursosDAO.read();

        for (Cursos curso : cursos) {
            Object[] rowData = {curso.getId_Curso(), curso.getNome_Curso(), curso.getArea()};
            tableModel.addRow(rowData);
        }
    }

    public void limparCampos() {
        txtId.setText("");
        txtCurso.setText("");
        txtArea.setText("");
        txtCurso.requestFocus();
    }

    private void alterarCampo() {
        int idCurso;
        String nomeCurso, nomeArea;

        idCurso = Integer.parseInt(txtId.getText());
        nomeCurso = txtCurso.getText();
        nomeArea = txtArea.getText();

        Cursos objCurso = new Cursos();
        objCurso.setId_Curso(idCurso);
        objCurso.setNome_Curso(nomeCurso);
        objCurso.setArea(nomeArea);

        CursosDAO objCursosdao = new CursosDAO();
        objCursosdao.alterarCompos(objCurso);
    }

    private void deletarCampo() {
        int idCurso;
        idCurso = Integer.parseInt(txtId.getText());
        Cursos objCurso = new Cursos();
        objCurso.setId_Curso(idCurso);

        CursosDAO objCursosdao = new CursosDAO();
        objCursosdao.deletarCampo(objCurso);
    }

